from replit import clear
import random
chosen_word = random.choice(word_list)
word_length = len(chosen_word)
end_of_game = False
print(chosen_word)
lives = 6
display = []
group = []
for i in range(word_length):
    display.append('_')
while end_of_game==False:
    guess = input("Enter a word: ").lower()

    for i in range(word_length):
        if guess == chosen_word[i]:
            display[i]=guess
    if guess not in group:
        if guess not in chosen_word:
            lives -= 1
        group.append(guess)
    else:
        print("You have already entered that word.")
    clear()
    print(" ".join(display))
    #print("Live= ",lives)
    #print(group)
    print(stages[lives])
    # Defining end condition
    if lives==0:
        end_of_game = True
        print("You lose")
    elif '_' not in display:
        end_of_game = True
        print("You win.")
    else:
        end_of_game = False